    private void btnSendEmailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendEmailActionPerformed
        String text = emailTxtFiled.getText();
        if (emailTxtFiled.getText().trim().equals("")) {
            JOptionPane.showConfirmDialog(null, "Enter a message", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SendMail(accountBillingRequest.getPatient().getPatientEmail(), text);
        JOptionPane.showMessageDialog(null, "Billing information sent to " + accountBillingRequest.getPatient().getPatientFirstName(), "Success", JOptionPane.INFORMATION_MESSAGE);
        accountBillingRequest.setStatus("Patient Transaction Completed");
        
        jLabel1.setIcon(null);
        jLabel1.revalidate();
        
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 2];
        ProcessMedicalBillingsJPanel awjp = (ProcessMedicalBillingsJPanel) component;
        awjp.populateTable();
        awjp.populateInsuranceClaimTable();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
        
    }//GEN-LAST:event_btnSendEmailActionPerformed

