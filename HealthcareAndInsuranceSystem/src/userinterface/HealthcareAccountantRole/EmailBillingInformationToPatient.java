    private void SendMail(String toMail, String text) {
        Properties properties = new Properties();

        // Setup mail server
        properties.put("mail.smtp.host", "smtp.gmail.com");
        properties.put("mail.smtp.socketFactory.port", "465");
        properties.put("mail.smtp.socketFactory.class",
                "javax.net.ssl.SSLSocketFactory");
        properties.put("mail.smtp.from", "healthandinsurance6100@gmail.com");
        properties.put("mail.smtp.auth", "true");
        properties.put("mail.smtp.port", "465");    

        Session session = Session.getDefaultInstance(properties, new javax.mail.Authenticator() {
            protected javax.mail.PasswordAuthentication getPasswordAuthentication() {
                return new javax.mail.PasswordAuthentication("healthandinsurance6100@gmail.com", "detlqvhhikuawhlr");
            }
        });

        try {
            MimeMessage message = new MimeMessage(session);
            message.setFrom(new InternetAddress("healthandinsurance6100@gmail.com"));
            message.addRecipient(Message.RecipientType.TO, new InternetAddress(toMail));
            message.setSubject("Patient Billing Information");

            message.setContent("<img src=\"http://aquariusmedical.ca/wp-content/themes/aquarius/img/inner-banner1.png\" style=\"width:950px;height:200px;\"><h1><right>" + enterprise.getName() + "</right></h1>"
                    + "<p>Dear " + accountBillingRequest.getPatient().getPatientFirstName() + ",</p>"
                    + "<p>Your treatment was completed. Please find below the final billing details:</p>"
                    + "<p>Total Payable Amount" + "        : $" + amount + "</p>"
                    + "<p>" + emailTxtFiled.getText() + "</p>"
                    + "<p>Regards,</p>"
                    + "<p>" + userAccount.getEmployee().getEmpName() + "</p><br/>", "text/html");

            Transport.send(message);
        } catch (MessagingException mex) {
            mex.printStackTrace();
        }
    }

